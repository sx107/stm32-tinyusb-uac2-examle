// Audio ring buffer. This is what you output to SAI, try to keep the read and write buffer at a constant distance using feedback.
#define AUDIOBUF_SIZE 1024
int32_t audio_buffer[AUDIOBUF_SIZE + (AUDIO_FIFO_SIZE / 4)];
uint32_t audio_buffer_pos = 0;

int main(void)
{

  HAL_Init();

  SystemClock_Config();

  MX_GPIO_Init();
  MX_USB_OTG_HS_PCD_Init();
  tusb_init();

  while (1)
  {
    /* USER CODE END WHILE */
	  tud_task();
	  if (uac2_initialized()) {
		  tu_fifo_t* fifo = uac2_get_out_fifo();
		  uint32_t cnt = tu_fifo_count(fifo);
		  tu_fifo_read_n(fifo, &audio_buffer[audio_buffer_pos], cnt);

		  if (audio_buffer_pos + cnt / 4 >= AUDIOBUF_SIZE) {
			  // Handle wrap-around - copy data from the end to the beginning
			  uint32_t overflow = 1 + audio_buffer_pos + cnt / 4 - AUDIOBUF_SIZE;
			  memcpy(&audio_buffer[0], &audio_buffer[AUDIOBUF_SIZE], overflow);
			  audio_buffer_pos += cnt / 4;
			  audio_buffer_pos -= AUDIOBUF_SIZE;
		  } else {
			  audio_buffer_pos += cnt / 4;
		  }

	  }

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

